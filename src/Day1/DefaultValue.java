package Day1;

public class DefaultValue {
	byte b;
	short s;
	int i;
	long l;
	float f;
	double d;
	char ch;
	boolean bu;
	
	public static void main(String[] args) {
		
		
		DefaultValue a= new DefaultValue();
		System.out.println("default value of byte is : " +(a.b));
		System.out.println("default value of short is : " +(a.s));
		System.out.println("default value of integer is : " +(a.i));
		System.out.println("default value of long is : " +(a.l));
		System.out.println("default value of float is : " +(a.f));
		System.out.println("default value of double is : " +(a.d));
		System.out.println("default value of char is : " +(a.ch));
		System.out.println("default value of boolean is : " +(a.bu));
		

	}

}
